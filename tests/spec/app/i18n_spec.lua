local i18n = require("app.i18n")

local function capture_logger()
    local buffer = {}
    return buffer, function(msg)
        buffer[#buffer + 1] = msg
    end
end

describe("app.i18n", function()
    before_each(function()
        i18n._reset()
        i18n._set_logger(function() end) -- silence by default
    end)

    describe("get_locale / set_locale", function()
        it("defaults to English", function()
            assert.are.equal("en", i18n.get_locale())
        end)

        it("can switch between bundled locales", function()
            i18n.set_locale("ru")
            assert.are.equal("ru", i18n.get_locale())
            i18n.set_locale("pl")
            assert.are.equal("pl", i18n.get_locale())
            i18n.set_locale("uk")
            assert.are.equal("uk", i18n.get_locale())
        end)

        it("rejects an unknown locale", function()
            assert.has_error(function()
                i18n.set_locale("xx")
            end)
        end)

        it("rejects a non-string locale code", function()
            assert.has_error(function()
                i18n.set_locale(42)
            end)
        end)
    end)

    describe("t()", function()
        it("looks up a key in the active locale", function()
            assert.are.equal("Thousand", i18n.t("app.title"))
            assert.are.equal("New Game", i18n.t("scene.menu.new_game"))
        end)

        it("looks up the same key under each bundled locale", function()
            for _, code in ipairs({ "en", "ru", "pl", "uk" }) do
                i18n.set_locale(code)
                assert.are.equal("Thousand", i18n.t("app.title"))
            end
        end)

        it("interpolates %{name} placeholders from a params table", function()
            assert.are.equal("Welcome, Alice!", i18n.t("greeting.welcome", { name = "Alice" }))
        end)

        it("preserves an unsubstituted placeholder when params omits the key", function()
            assert.are.equal("Welcome, %{name}!", i18n.t("greeting.welcome", {}))
        end)

        it("returns the raw string when params is nil", function()
            assert.are.equal("Welcome, %{name}!", i18n.t("greeting.welcome"))
        end)

        it("rejects a non-string key", function()
            assert.has_error(function()
                i18n.t(42)
            end)
        end)
    end)

    describe("fallback to en", function()
        it("returns the en value when the active locale lacks the key", function()
            i18n._set_locale_table("en", { ["only_en"] = "Hello from en" })
            i18n._set_locale_table("ru", { ["other"] = "Other" })
            i18n.set_locale("ru")
            assert.are.equal("Hello from en", i18n.t("only_en"))
        end)

        it("interpolates the fallback value too", function()
            i18n._set_locale_table("en", { ["greet"] = "Hello, %{name}!" })
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            assert.are.equal("Hello, Bob!", i18n.t("greet", { name = "Bob" }))
        end)

        it("returns the bare key when missing in both active and en", function()
            i18n._set_locale_table("en", {})
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            assert.are.equal("does.not.exist", i18n.t("does.not.exist"))
        end)

        it("does not fall back when active is already en", function()
            i18n._set_locale_table("en", {})
            assert.are.equal("missing", i18n.t("missing"))
        end)
    end)

    describe("missing-key logging", function()
        it("logs once when a key is missing in the active locale", function()
            local logs, fn = capture_logger()
            i18n._set_logger(fn)
            i18n._set_locale_table("en", { ["x"] = "X" })
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            i18n.t("x")
            assert.are.equal(1, #logs)
            assert.is_truthy(logs[1]:find("missing key", 1, true))
            assert.is_truthy(logs[1]:find('"ru"', 1, true))
        end)

        it("does not log a second time for the same (locale, key) pair", function()
            local logs, fn = capture_logger()
            i18n._set_logger(fn)
            i18n._set_locale_table("en", { ["x"] = "X" })
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            i18n.t("x")
            i18n.t("x")
            i18n.t("x")
            assert.are.equal(1, #logs)
        end)

        it("logs en gap separately when the key is missing in both locales", function()
            local logs, fn = capture_logger()
            i18n._set_logger(fn)
            i18n._set_locale_table("en", {})
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            i18n.t("nope")
            assert.are.equal(2, #logs)
            -- Order: active first, then en.
            assert.is_truthy(logs[1]:find('"ru"', 1, true))
            assert.is_truthy(logs[2]:find('"en"', 1, true))
        end)

        it("logs distinct keys distinctly", function()
            local logs, fn = capture_logger()
            i18n._set_logger(fn)
            i18n._set_locale_table("en", { ["a"] = "A", ["b"] = "B" })
            i18n._set_locale_table("ru", {})
            i18n.set_locale("ru")
            i18n.t("a")
            i18n.t("b")
            assert.are.equal(2, #logs)
        end)
    end)
end)
